ORG 100H

.DATA


.CODE


NEXT:
; SET CURSOR POSITION AT DL, DH
MOV AH, 02h
INT 10h

MOV AL, 179
MOV BH, 0       ; MUST BE 0 TO WORK
MOV CX, 1       ; NUMBER OF CHAR PER TIME
MOV AH, 09h
INT 10h

INC BL          ; INCREMENT THE ATTIRBUTE FOR THE COLOR

CMP BL, 255
    JE STOP_NEXT 

CMP DL, 15
    JE NEW_LINE
    INC DL
    JMP NEXT
NEW_LINE:
    INC DH         
    MOV DL, 0
JMP NEXT
STOP_NEXT: